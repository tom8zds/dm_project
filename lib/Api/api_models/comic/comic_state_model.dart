enum Status { ONGOING, FINISH }

final statusValues = EnumValues({"连载中": Status.ONGOING, "已完结": Status.FINISH});

class EnumValues<T> {
  Map<String, T> map;
  Map<T, String> reverseMap;

  EnumValues(this.map);

  Map<T, String> get reverse {
    if (reverseMap == null) {
      reverseMap = map.map((k, v) => new MapEntry(v, k));
    }
    return reverseMap;
  }
}
